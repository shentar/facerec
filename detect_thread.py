from threading import Thread


class DetectThread(Thread):
    def __init__(self, func, args):
        super(DetectThread, self).__init__()
        self.func = func
        self.args = args
        self.result = None

    def run(self):
        self.result = self.func(*self.args)

    def get_result(self):
        try:
            return self.result
        except Exception:
            return None
